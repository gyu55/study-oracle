-- 2) 상품을 구매한 20 ~ 30대 구매자의 수 조회
SELECT TBU.USER_NAME "구매자 이름", TBU.USER_AGE "구매자 나이"
FROM TBL_ORDER TBO
JOIN TBL_USER TBU
ON TBO.USER_ID = TBU.ID
GROUP BY TBU.USER_NAME, TBU.USER_AGE
HAVING TBU.USER_AGE >= 20 AND TBU.USER_AGE <= 39;

-- 3) 뉴발란스를 구매한 20대 구매자의 이름 조회
SELECT TBU.USER_NAME
FROM TBL_USER TBU
JOIN TBL_ORDER TBO 
ON TBU.ID = TBO.USER_ID
JOIN TBL_PRODUCT TBP 
ON TBO.PRODUCT_ID = TBP.ID
WHERE TBP.PRODUCT_BRAND = '뉴발란스'
  AND TBU.USER_AGE BETWEEN 20 AND 29;

-- 4) 여성의 나이대별 평균 지출 금액 조회
SELECT * 
FROM TBL_ORDER TBO
JOIN TBL_PRODUCT
SELECT SUBSTR(USER_AGE, 1, 1) || '0대' AS "나이대",
       AVG(TOTAL_PRICE) AS "평균 지출 금액"
FROM (
    SELECT TBU.USER_NAME, TBU.USER_AGE, SUM(TBP.PRODUCT_PRICE) AS TOTAL_PRICE
    FROM TBL_USER TBU
    JOIN TBL_ORDER TBO ON TBU.ID = TBO.USER_ID
    JOIN TBL_PRODUCT TBP ON TBO.PRODUCT_ID = TBP.ID
    WHERE TBU.USER_GENDER = '여'
    GROUP BY TBU.USER_NAME, TBU.USER_AGE
)
GROUP BY SUBSTR(USER_AGE, 1, 1)
ORDER BY "나이대";

