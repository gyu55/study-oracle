-- 6) 가장 매출이 적은 상품을 구매한 사용자의 이름과, 상품명 조회

SELECT TBU.USER_NAME "사용자 이름", TBP.PRODUCT_NAME "상품명"
FROM TBL_USER TBU
JOIN TBL_ORDER TBO
ON TBO.USER_ID = TBU.ID
JOIN TBL_PRODUCT TBP 
ON TBO.PRODUCT_ID = TBP.ID
WHERE TBO.PRODUCT_ID = (
	SELECT PRODUCT_ID
	FROM (
		SELECT TBP.PRODUCT_NAME, SUM(TBP.PRODUCT_PRICE), TBO.PRODUCT_ID
		FROM TBL_ORDER TBO
		JOIN TBL_PRODUCT TBP
		ON TBO.PRODUCT_ID = TBP.ID
		JOIN TBL_USER TBU
		ON TBO.USER_ID = TBU.ID
		GROUP BY PRODUCT_NAME, PRODUCT_ID
		ORDER BY SUM(TBP.PRODUCT_PRICE) ASC
		FETCH FIRST 1 ROWS ONLY
	)
);
