-- 9) 가장 적게 판매된 상품의 이름
SELECT TBP.PRODUCT_NAME "가장 적게 판매된 상품의 이름"
FROM TBL_ORDER TBO
RIGHT JOIN TBL_PRODUCT TBP
ON TBO.PRODUCT_ID = TBP.ID
GROUP BY TBP.PRODUCT_NAME, TBO.PRODUCT_ID
HAVING COUNT(TBO.PRODUCT_ID) = (
   SELECT MIN(SELL_COUNT)
   FROM (
      SELECT COUNT(TBO.PRODUCT_ID) "SELL_COUNT"
      FROM TBL_ORDER TBO
      RIGHT JOIN TBL_PRODUCT TBP
      ON TBO.PRODUCT_ID = TBP.ID
      GROUP BY TBP.PRODUCT_NAME, TBO.PRODUCT_ID
   )
);

